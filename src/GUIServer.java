import java.io.*;
import java.net.*;

//----------------------------------------------------------------------------//


public class GUIServer extends GUIChat
{

   //-------------------------------------------------------------------------//

   public GUIServer()
   {
      super();
   }

  //-------------------------------------------------------------------------//

  public Socket connect() throws IOException
  {
     ServerSocket server = new ServerSocket(8080);

     return server.accept();
   }

   //-------------------------------------------------------------------------//


}
